package cn.edu.shiep.model.record;

public class OkClass {

	private String idOfClass;
	private String classAddress;
	private String className;
	private String classTimeNo;
	private String dayOfWeek;
	private String teacherOfClass;
	private String isAddDelete;
	public String getIdOfClass() {
		return idOfClass;
	}
	public void setIdOfClass(String idOfClass) {
		this.idOfClass = idOfClass;
	}
	public String getClassAddress() {
		return classAddress;
	}
	public void setClassAddress(String classAddress) {
		this.classAddress = classAddress;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getClassTimeNo() {
		return classTimeNo;
	}
	public void setClassTimeNo(String classTimeNo) {
		this.classTimeNo = classTimeNo;
	}
	public String getDayOfWeek() {
		return dayOfWeek;
	}
	public void setDayOfWeek(String dayOfWeek) {
		this.dayOfWeek = dayOfWeek;
	}
	public String getTeacherOfClass() {
		return teacherOfClass;
	}
	public void setTeacherOfClass(String teacherOfClass) {
		this.teacherOfClass = teacherOfClass;
	}
	public String getIsAddDelete() {
		return isAddDelete;
	}
	public void setIsAddDelete(String isAddDelete) {
		this.isAddDelete = isAddDelete;
	}
	
	
	
	
}
